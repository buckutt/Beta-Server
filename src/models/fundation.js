import thinky from '../thinky';

let type = thinky.type;

let Fundation = thinky.createModel('Fundation', {
    id       : type.string().optional(), // Optional => not specified in bodies but generated by RethinkDB
    name     : String,
    website  : String,
    mail     : String,
    createdAt: type.date().default(new Date()),
    editedAt : Date,
    isRemoved: type.boolean().default(false)
}, {
    enforce_missing: true,
    enforce_extra  : 'remove',
    enforce_type   : 'strict'
});

Fundation.pre('save', function (next) {
    this.editedAt = new Date();
    next();
});

Fundation.ensureIndex('name');
Fundation.ensureIndex('createdAt');
Fundation.ensureIndex('editedAt');

Fundation.associate = models => {
    models.Fundation.hasMany(models.Price, 'prices', 'id', 'fundationId');
    models.Fundation.hasMany(models.Purchase, 'purchases', 'id', 'fundationId');
};

export default Fundation;
